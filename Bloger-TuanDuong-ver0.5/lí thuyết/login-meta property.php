<p>Khái niệm thẻ Meta Property
Đây là một trong những thẻ meta quan trọng nhằm khai báo thuộc tính dữ liệu có cấu trúc của một website.

Các thuộc tính thẻ Meta Property
Có rất nhiều thuộc tính để ta khai báo, tùy thuộc lĩnh vực website của bạn là gì một vài thuộc tính cơ bản có cấu trúc :

Mã:
<meta property="og:value" content="content value" />

Value ở đây có thể: url, description, site_name, audio,song, article….

Có nên sử dụng thẻ Meta Property

Một đơn vị làm seo hoặc thiết kế web thì không nên bỏ qua các thẻ meta này, bên cạnh các thẻ meta description, keywords, title thì việc sử dụng các thẻ này giúp bot google phân loại và nhận dạng website của bạn 1 cách chính xác và nhanh, tăng độ trust của website.

Một số thẻ cơ bản

Mã:
<meta property="og:locale" content="vi_VN" />
<meta property="og:type" content="website" />
<meta property="og:title" content="Chia sẻ phần mềm miễn phí" />
<meta property="og:url" content="http://vietsoftwares.com/" />
<meta property="og:site_name" content="Phần mềm miễn phí" />
<meta property="og:image" content="http://vietsoftwares.com/images/logo.jpg"/>
</p>
